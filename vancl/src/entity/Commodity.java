package entity;

public class Commodity {
	private int commodityId;
	private String commodityName;
	private int commodityCategoryId;
	private double commodityPrice;
	private String commodityDate;
	private String commodityPraise;
	private String commoditySales;
	private String commodityImageUrl;
	private String commodityColor;
	private String commoditySize;
	public int getCommodityId() {
		return commodityId;
	}
	public void setCommodityId(int commodityId) {
		this.commodityId = commodityId;
	}
	public String getCommodityName() {
		return commodityName;
	}
	public void setCommodityName(String commodityName) {
		this.commodityName = commodityName;
	}
	public int getCommodityCategoryId() {
		return commodityCategoryId;
	}
	public void setCommodityCategoryId(int commodityCategoryId) {
		this.commodityCategoryId = commodityCategoryId;
	}
	public double getCommodityPrice() {
		return commodityPrice;
	}
	public void setCommodityPrice(double commodityPrice) {
		this.commodityPrice = commodityPrice;
	}
	public String getCommodityDate() {
		return commodityDate;
	}
	public void setCommodityDate(String commodityDate) {
		this.commodityDate = commodityDate;
	}
	public String getCommodityPraise() {
		return commodityPraise;
	}
	public void setCommodityPraise(String commodityPraise) {
		this.commodityPraise = commodityPraise;
	}
	public String getCommoditySales() {
		return commoditySales;
	}
	public void setCommoditySales(String commoditySales) {
		this.commoditySales = commoditySales;
	}
	public String getCommodityImageUrl() {
		return commodityImageUrl;
	}
	public void setCommodityImageUrl(String commodityImageUrl) {
		this.commodityImageUrl = commodityImageUrl;
	}
	public String getCommodityColor() {
		return commodityColor;
	}
	public void setCommodityColor(String commodityColor) {
		this.commodityColor = commodityColor;
	}
	public String getCommoditySize() {
		return commoditySize;
	}
	public void setCommoditySize(String commoditySize) {
		this.commoditySize = commoditySize;
	}
}
